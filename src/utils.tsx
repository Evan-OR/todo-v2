export const checkIfTodoExists = (todo: ToDo, todoArray: ToDo[]) => {
  for (let t of todoArray) {
    if (t.id === todo.id) {
      return true;
    }
  }

  return false;
};

export const sortTodos = (ff: FilterType, todos: ToDo[]): ToDo[] => {
  //If function was called with no filter set just return
  if (ff === 'None') return todos;

  let filteredArray: Array<ToDo[]> = [];

  switch (ff) {
    case 'Low to High Priority':
      filteredArray = sortLowToHighPriority(todos);
      break;
    case 'High to Low Priority':
      filteredArray = sortLowToHighPriority(todos).reverse();
      break;
    case 'Colour':
      filteredArray = sortByColourOrIcon(todos, true);
      break;
    case 'Icon':
      filteredArray = sortByColourOrIcon(todos, false);
      break;
    default:
      return todos;
  }

  let joinedArrays: ToDo[] = [];

  for (let arr of filteredArray) {
    joinedArrays = joinedArrays.concat(...arr);
  }

  return joinedArrays;
};

const sortLowToHighPriority = (todos: ToDo[]): Array<ToDo[]> => {
  //SECOND SORT BREFORE RETURNING
  let priorityArrays: Array<ToDo[]> = [[], [], [], []];
  //Low to high sort
  for (let el of todos) {
    priorityArrays[el.priority].push(el);
  }

  return priorityArrays;
};

const sortByColourOrIcon = (todos: ToDo[], sortingByColour: boolean): Array<ToDo[]> => {
  //SECOND SORT BREFORE RETURNING
  let splitArrays: Array<ToDo[]> = [];
  let sortProperty: 'colour' | 'iconId' = sortingByColour ? 'colour' : 'iconId';

  for (let todo of todos) {
    //Check if thing is in the array
    let typeInArray = false;
    for (let i = 0; i < splitArrays.length; i++) {
      if (todo[`${sortProperty}`] === splitArrays[i][0][`${sortProperty}`]) {
        typeInArray = true;
        splitArrays[i].push(todo);
      }
    }
    //Wasn't found in array
    if (!typeInArray) {
      splitArrays.push([todo]);
    }
  }

  return splitArrays;
};

type StorageType = 'todos' | 'completedTodos';
export const writeToLocalStorage = (todos: ToDo[], storageName: StorageType): void => {
  localStorage.setItem(storageName, JSON.stringify(todos));
};
export const readLocalStorage = (storageName: StorageType): ToDo[] => {
  let storage = localStorage.getItem(storageName);

  if (storage !== null) {
    return JSON.parse(storage);
  } else {
    return [];
  }
};
